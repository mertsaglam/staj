public class Location {
    public int x;
    public int y;
    public Location(int x,int y){
        this.x=x;
        this.y=y;
    }
    public int getX(){
        return x;
    }
    public void setX(int x){
        this.x = x;
    }
    public int getY(){
        return x;
    }
    public void setY(int y){
        this.y = y;
    }
}
